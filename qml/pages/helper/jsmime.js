function getMimesByExt(ext)
{
    return mimes[ext] || ["application/octet-stream", "text/plain"];
}

function getMimesByPath(path)
{
    var ext = path.substring(path.lastIndexOf(".") + 1);
    return getMimesByExt(ext);
}

function pathIsMime(path, mime)
{
    var mimes = getMimesByPath(path);
    for(var i = 0; i < mimes.length; i++)
        if(mime == mimes[i])
            return true;
    return false;
}

